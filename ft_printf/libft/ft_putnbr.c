#include "libft.h"
#include <unistd.h>
#include <stdio.h>

void	ft_putnbr(long n)
{
	if (n < 0)
	{
		n = -n;
		ft_putchar('-');
	}
	if (n >= 10)
		ft_putnbr(n / 10);
	ft_putchar(n % 10 + '0');
}
